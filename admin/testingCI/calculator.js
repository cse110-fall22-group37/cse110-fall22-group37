const mathOperations = {
    /**
     * Adds 2 numbers
     * @param {number} a 
     * @param {number} b 
     * @returns number which is the sum
     */
    sum: function(a,b){
        return a+b;
    },
    diff: function(a,b){
        return a-b;
    },
    product: function(a,b){
        return a*b
    }
}
module.exports = mathOperations